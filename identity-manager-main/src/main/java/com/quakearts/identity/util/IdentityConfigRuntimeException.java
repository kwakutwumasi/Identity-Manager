package com.quakearts.identity.util;

public class IdentityConfigRuntimeException extends RuntimeException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 5761899155374541058L;

	public IdentityConfigRuntimeException() {
		super();
	}

	public IdentityConfigRuntimeException(String message, Throwable cause) {
		super(message, cause);
	}

	public IdentityConfigRuntimeException(String message) {
		super(message);
	}

	public IdentityConfigRuntimeException(Throwable cause) {
		super(cause);
	}

}
